package amd.caveofprogramming.fl;

public class ForLoops {

	public static void main(String[] args) {
		
		for(int i = 0; i < 5; i++) {
			System.out.println("i:" + i);
		}
		System.out.println();
		for(int i = 7; i > 2; i--) {
			System.out.println("i:" + i);
		}
		
		System.out.println();
		for(int i = 3; i < 12; i++) {
			System.out.println("i:" + i);
		}
		
	}
	
}
