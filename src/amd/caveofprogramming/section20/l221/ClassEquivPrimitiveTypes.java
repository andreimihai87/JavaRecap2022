package amd.caveofprogramming.section20.l221;

public class ClassEquivPrimitiveTypes {

	public static void main(String[] args) {

		// Primitive Types - Equivalent Classes
		/*
		 * byte		- 	Byte
		 * short 	- 	Short
		 * int 		- 	Integer
		 * long		- 	Long
		 * 
		 * float 	- 	Float
		 * double 	- 	Double
		 * 
		 * char 	- 	Character
		 * boolean	- 	Boolean
		 */
		
		Integer intValue = 128;
		Integer intValue2 = 128;
		
		System.out.println(intValue.equals(intValue2));
		
	}

}
