public class ArraysOfStrings {

	public static void main(String[] args) {

		String[] shopingList = { "Potatoes", "Rice", "Pasta", "Bread" };

		System.out.println(shopingList[1]);
		System.out.println(shopingList[3]);

		shopingList[1] = "Tomatoes";

		System.out.println(shopingList[1]);
		System.out.println(shopingList[3]);

	}

}
